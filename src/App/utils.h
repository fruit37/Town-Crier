inline const char *homedir() {
  const char *home_dir;
  if ((home_dir = getenv("HOME")) == NULL) {
    home_dir = getpwuid(getuid())->pw_dir;  // NOLINT(runtime/threadsafe_fn)
  }

  return home_dir;
}

